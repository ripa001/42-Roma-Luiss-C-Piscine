int	ft_str_is_printable(char *str)
{	
	int	i;
	int	flag;

	flag = 1;
	i = 0;
	while (str[i] != 0)
	{
		if (str[i] >= 32 && str[i] <= 127)
			flag = 1;
		else
			return (0);
		i++;
	}
	return (flag);
}
